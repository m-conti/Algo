#include "libft.h"

int	ft_atoi(char *str)
{
	unsigned long	r;
	int				n;
	int				a;

	r = 0;
	n = 1;
	a = 0;
	while (str[a] == ' ' || str[a] == '\t' || str[a] == '\v' || str[a] == '\r'
	|| str[a] == '\n' || str[a] == '\f')
		a++;
	if (str[a] == '-')
		n = -1;
	if (str[a] == '+' || str[a] == '-')
		a++;
	while (str[a] - 48 >= 0 && str[a] - 48 <= 9)
	{
		if ((r = r * 10 + str[a++] - 48) > 9223372036854775807)
			return (n > 0 ? -1 : 0);
	}
	a = r * n;
	return (a);
}
