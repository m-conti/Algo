#include "libft.h"

int		ft_power(int const nb, unsigned int const pow)
{
	unsigned int	i;
	long			r;

	r = 1;
	i = 0;
	while (i++ < pow && r * nb <= 2147483647 && r * nb >= -2147483648)
		r *= nb;
	if (r * nb >= 2147483647 && r * nb <= -2147483648)
		return (0);
	return (r);
}
