#include "libft.h"

int		ft_index(const char *s, int c)
{
	int i;

	i = 0;
	if (!s)
		return (-1);
	while (s[i] != (char)c && s[i])
		i++;
	if (s[i] == (char)c)
		return (i);
	return (-1);
}
