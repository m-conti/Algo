#include "libft.h"

void	ft_strrev(char *str)
{
	char	a;
	int		i;
	int		n;

	i = -1;
	n = ft_strlen(str);
	while (++i < --n)
	{
		a = str[n];
		str[n] = str[i];
		str[i] = a;
	}
}
