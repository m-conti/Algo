#include "libft.h"

int		ft_putcstr(char const *s, char c)
{
	int		i;
	char	v;

	v = ft_index(s, c);
	if (v == 0)
		return (0);
	i = v > 0 ? v : ft_strlen(s);
	write(1, s, i);
	return (i);
}
