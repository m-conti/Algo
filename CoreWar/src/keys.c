#include "corewar.h"

void	deal_key(t_core *core, int ch)
{
	if (ch == KEY_END)
		exit (0);
	if (ch == KEY_UP)
	   core->v.delay += 10000;	
	if (ch == KEY_DOWN)
		if (core->v.delay > 10000)
			core->v.delay -= 10000;
}
